// IPCMessage.cpp : IPC Message 파서
//// -------------------------------------------------------------------------------- 
// [protocol spec]
//
// |------------------------------------------------------------------------------------|
// | Header    | Body                                                                   |
// |------------------------------------------------------------------------------------|
// | Header    | Command | Parameter                                                    | 
// |------------------------------------------------------------------------------------|
// | 1)  |  2) | 3)      | 4) | 5) ... | 6) ...                                         | 
// |------------------------------------------------------------------------------------|
//
// 1) Total Bytes (4 Bytes)  : Message 전체 길이 (bytes)
// 2) Reserved (8 Bytes)     : (Reserved)
// 3) Command (4 Bytes)      : Command 정의
// 4) Param Count (4 Bytes)	 : Param 갯수 
// 5) Param Length (? Bytes) : (4 Bytes * <Param Count>), Param Count 만큼 반복,
// 6) Param (? Bytes)        : Param Count 만큼, Param Length 길이로 반복.
//
// * Param 특이사항 
// a) Binary 형태로 저장한다. 
// b) 문자열은 <NUL> 을 포함한다.
// c) 문자열의 인코딩은 유지된다. ( MBCS, UTF16, UTF-8 등 )
// -------------------------------------------------------------------------------- 

// -------------------------------------------------------------------------------- 
// [protocol example]
//
// |------------------------------------------------------------------------------------|
// | Header  | Body                                                                     |
// |------------------------------------------------------------------------------------|
// | Header  | Command | Parameter                                                      | 
// |------------------------------------------------------------------------------------|
// | 61 | 0 | 100     | 3 | 17 | 12 | 4 | C:\Temp\File.TXT<NUL> | E:\File.TXT<NUL> | 8 |
// |------------------------------------------------------------------------------------|
//
// * 총 길이 : 61 bytes
// * Command : 100
// * Parameter 갯수 : 3 개
// * 1st Param (17 bytes) : C:\Temp\File.TXT<NUL> 
// * 2nd Param (12 bytes) : E:\File.TXT<NUL> 
// * 3rd Param (4 bytes) : 8
// -------------------------------------------------------------------------------- 
//
